"""

Задача 5. Сложная проверка с регулярными выражениями

Условие:
Напишите программу, которая анализирует сложность пароля используя тернарный оператор и regex.

Логика:

    Если пароль содержит цифры И 
    буквы в верхнем и нижнем регистре И длиной >= 8 -> "Надежный пароль"
    Иначе если пароль содержит цифры ИЛИ буквы в разных регистрах -> 
    "Средний пароль"  
    Иначе -> "Слабый пароль"

Пример входных данных:
    Пароль: "Secure123"
"""

### ваше решение здесь ###
password = "Secure123"
has_digit = any(char.isdigit() for char in password)
password_length = len(password)
has_upper = bool(re.search(r'[A-Z]', password))
has_lower = bool(re.search(r'[a-a]', password))
if has_digit and has_upper and has_lower and password_length > 8:
    print('great password')
elif has_digit or has_upper or has_lower:
    print('medium password')
else:
    print('bad password')
